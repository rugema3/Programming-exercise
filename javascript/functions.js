#!/usr/bin/env node

/**
 * multiply - This functions multiplies two numbers.
 *
 * @param {number} num1 - The number to be multiplied
 * @param {number} num2 - The number to be multiplied
 * @returns {number} result - The product of num1 and num2
 */

function multiply(num1, num2)
{
	let result = num1 * num2;
	return result;
}

console.log(multiply(10, 3));
